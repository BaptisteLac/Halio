export function getSpeciesById(id: string): Species | undefined {
  return SPECIES.find((s) => s.id === id);
}

export function getSpeciesBySlug(slug: string): Species | undefined {
  return SPECIES.find((s) => s.slug === slug);
}

export function getSpeciesInSeason(month: number): Species[] {
  return SPECIES.filter((s) => {
    if (s.season.start <= s.season.end) {
      return month >= s.season.start && month <= s.season.end;
    }
    // Espèce dont la saison traverse le changement d'année (ex: sole: oct→mars)
    return month >= s.season.start || month <= s.season.end;
  });
}
